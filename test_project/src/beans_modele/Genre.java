package beans_modele;

public class Genre {

	private int id_genre;
	private String nom;
		
	
	public int getId_genre() {
		return id_genre;
	}
	public void setId_genre(int id_genre) {
		this.id_genre = id_genre;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom_genre) {
		this.nom = nom_genre;
	}
	

}
